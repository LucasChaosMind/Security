package model;

public class ChavePublica {
    int e;
    int phi;

    public ChavePublica(int e, int phi) {
        this.e = e;
        this.phi = phi;
    }

    public int getE() {
        return e;
    }

    public void setE(int e) {
        this.e = e;
    }

    public int getPhi() {
        return phi;
    }

    public void setPhi(int phi) {
        this.phi = phi;
    }
}
